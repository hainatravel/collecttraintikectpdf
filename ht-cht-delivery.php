  <h2><a name="board_train" id="board_train"></a><a name="Part-1_How_to" id="Part-1_How_to"></a>Part-1 How to board a train </h2>
  <h3>1. Make sure you have everything ready </h3>
  <p align="left">  <strong>Your  passport(s) and paper ticket(s) </strong>are the two essential things for boarding, along with your luggage and other stuff. You will need to present paper ticket(s) twice before boarding: first at the security check and then at the ticket check, and passport(s) only once, at the security check.   </p>
  <p>Putting your passport(s) in a fanny pack or mini bag can be handy. You should always be careful to keep it safe after checking.</p>
  <p>Dining on a train, no matter whether a snack from the trolley or a made-to-order meal in the dining car, is hardly the same as eating in a restaurant. For long-distance travel, we suggest you bring some food, cookies, instant noodles, fruit and drink. </p>
  <p>What’s more, there’s no toilet paper, disposable toilet seat covers or flip-flops on trains, so better pack them if required.</p>
  <h3>2. Pass the security check </h3>
  <p align="left">A security check is required before entering the waiting hall, which at some stations even happens before entering the station building. Station staff will scan you with an X-ray device and your luggage will go through a scanning machine. </p>
  <p align="left">The check is less hassle and easier than that at the airport. For example, common liquid items are not banned and lighters can be brought onto the train.</p>
  <p align="left">Prohibited things that are not allowed to take in: firearms (guns, knives, or ammunition, etc), explosives (including fireworks and pyrotechnics) and imitation explosives or devices, items containing incapacitating gases, liquids or other substances (including CS gas sprays), inflammable substances except in minimal quantities (eg. lighters), butane gas, any items that are prohibited or limited by the government, live animals and insects or anything that can do harm to other passengers and public hygiene. </p>
  <p align="left">Excess luggage (exceeding the regulation limitations) should not be brought onto the train either.</p>
  <h3>3. Find the right place to wait </h3>
  <p align="left">The waiting hall is where you wait for boarding. Most stations have different waiting sections for different trains, but some have all passengers waiting in a big hall. For most high-speed trains, the appointed waiting section is stated on the paper ticket (see examples in the pictures below).</p>
