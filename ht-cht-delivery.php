  <h2>Part-2 Travel tips </h2>
  <h3>1. Safety</h3>
  <p align="left">Traveling by train in China is fairly safe. However, as with traveling by air or barefoot, this advantage can be blunted if safety awareness is not a priority. Here are some tips for train safety. <br />
    (1) Be advised that you should always keep your belongings in view and never leave valuable items out in the open. Your cash, credit cards, travel documents and other important items should be separated and kept safe. <br />
    &nbsp;&nbsp;&nbsp;&nbsp;Normally, a small amount of cash, perhaps 100RMB, will be more than enough for your needs on the train (e.g. food and other stuff). Have some small notes handy, so you don’t have to rummage in your bags or luggage.<br />
    (2) It is the adults’ job to restrain young kids from getting too excited about anything, for children may run around on the platform or in the carriage, or fiddle about with the bathroom doors or water faucet. <br />
   &nbsp;&nbsp;&nbsp;&nbsp; Normally the stations don’t have special passages or boarding platforms for senior travelers, but it is unlikely that a senior will be jostled or pushed. Apart from on high-speed trains which travel smoothly, senior travelers may want to avoid walking around on the train, as sometimes harsh jarring or sudden braking can happen.<br/>
    
    (3) Tap water is not for drinking. Always drink bottled water or get water at the drinking pantry. Let your children know this before they ask for a drink. <br />
    (4)	Keeping your wits about you might be easy during the daytime or on short trips. On some overnight train rides, however, there is a chance that things might be stolen or lost.<br /> 
    &nbsp;&nbsp;&nbsp;&nbsp;Use the lock on your suitcase. If your bag or rucksack has a strap, clip it up onto the railing by the bunk or onto your arm before you go to sleep. This will wake you up if the bag or rucksack moves. Stash your money, cards and travel documents inside your jacket and zip it up. Of course, lock up the compartment if possible.
  </p>
 
 <h3>2. Luggage limits </h3>
  <p align="left">
In theory, the luggage limit on Chinese trains is 20 kg for adults and 10 kg for children. No one, however, weighs or measures your bags, so if your luggage goes through the security check, you'll be fine. If you really want to transport vast quantities of luggage, you can purchase baggage tickets for bags in excess of the official limits.
  </p>
  
 <h3>3. Red-cap porter service </h3>
  <p align="left"> 
  If you take heavy luggage or a big suitcase with you, you might want to find a red-cap porters at the train station to help you carry your luggage onto the train. Each piece will cost about 10 or 20 yuan.
  </p>
  <h3>4. Toilets</h3>
  <p align="left">All the regular non-high-speed trains are equipped with Japanese-style squat toilets, while all the high-speed trains have western-style loos. Bathrooms are located at both ends of every car.</p>
  <p>Note that toilets will be temporarily closed 10 minutes before the train reaches a station and during the time it stays there. So just go whenever nature calls, coz you don’t know when you’re going to reach a station. And there may be a long waiting line, too.</p>
  <p>On high-speed trains, used tissues and other trash should be put into the trash can. However, on non-high-speed ones, tissue and other trash can be flushed down by treading on a pedal. Always bring valuables with you. You can put them on the door hook while using the toilet.</p>
  <p>Bathrooms on high-speed trains have disposable paper seat cover and tissues. In the washroom, there is soap (or liquid soap) and paper for drying hands. Ask a steward for a refill if the paper is used up.</p>
  <p>None of these things is offered on regular non-high-speed trains. Be mentally prepared, for the squat toilets can be really nasty on some of the long-haul trains. Thus, a pack of baby wipes or hand sterilizer and some tissues should be amongst your baggage.</p>
  <p>Young kids and senior travelers are advised to have a guardian on hand when using the toilet. Senior travelers should use the hand railing inside the bathroom. And watch out for doors slamming and pinching fingers. Floors can be slippery, so watch your step and walk slowly.</p>
  <h3>5.  Preparing to disembark </h3>
