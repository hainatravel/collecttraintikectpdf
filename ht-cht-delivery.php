  <h3>4. Have your ticket(s) checked </h3>
  <p align="left"><strong>Ticket  checking stops 5 minutes before the departure time</strong>. Make sure you get everything ready before checking. Ticket barriers at the exit of every waiting section link to the boarding platform. Normally there is a LED screen above the barrier telling what the current situation is: waiting (正在候车), ticket checking (正在检票), ticket checking over (停止检票).</p>
  <p>People tend to get anxious once “ticket checking” has begun, but knowing where you are heading should help: with a blue-colored paper ticket, you should pass through the automatic checking machine; the red-colored tickets are to be shown to ticket inspectors and get pinched by a pair of ticket scissors. Put away your ticket(s) once checking has finished.</p>
  <p>At some railway stations, waiting sections are not divided. In that case, show this sentence to any station staff or passer-by, while showing your ticket(s), to find your waiting line:</p>
