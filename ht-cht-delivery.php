  <h3>5. Get onto the platform </h3>
  <p align="left">Under some circumstances, you may need to walk up and down stairs to get onto the platform. At some recently-built or renovated stations, escalators and elevators will make this walk easier. Unfortunately some train stations do not have an escalator or elevator therefore you may need to walk up and down stairs to get onto the platform. A LED screen will show the direction of your designated platform by highlighting the train number, indicating which stairs to walk down or which direction to go. </p>
  <p align="center"><br />
    <img border="0" width="567" height="274" src="howTo_clip_image011.jpg" /></p>
  <p align="left"><br />
  Unlike in Europe, boarding platforms in China don’t have gates. If you are traveling with children, keep an eye on them due to the large crowds. There may be a lot of pushing and shoving! </p>
    <p>There may well be trains on either side of a platform. Board the train with number matching your paper ticket. Ticket inspectors who stand by the gates of different cars will request a ticket check, but not an ID check. Double-check with the inspector if you are not sure it’s the right train.</p>
