  <h2 align="left"><a name="collect_tickets" id="collect_tickets"></a><a name="Part-1_How_to" id="Part-1_How_to"></a>Parte-1 Cómo comprar billetes de tren </h2>
  <h3 align="left">1. Tiempo para la recogida de billetes</h3>
  <p align="left">  Estar preparado. Este lema de Boy Scout se aplica aquí también. Tenga en cuenta que tienes que recoger el billete de papel y subir las maletas. Es mucho más inteligente obtener su billete de papel<strong> por lo menos 3 horas antes de la salida</strong> que en el último minuto. Y tenga en cuenta <strong>las horas comerciales</strong>, las ventanas para recogida de billetes en las estaciones de tren están abiertas de 8:00 a 22:00. Los agentes de venta de billetes (代售点) no están para la recogida de estos.</p>
 <p align="left"> En algunas estaciones de ferrocarril, el control de seguridad es necesario antes de poder entrar en el edificio real de la estación y retirar los billetes de tren. Se les podría escanear con un escáner de rayos x en la entrada del hall de la estación pero es poco probable que le solicitar sus certificados de ID o billetes de tren.</p>
  <h3 align="left">2. Dos cosas en la mano </h3>
  <p>Cada reserva que se emitió con éxito viene con un determinado número de recogida. Si usted tiene múltiples reservas, usted recibirá números de recogida diferentes para cada reserva. Usted encontrará al principio de este mensaje. Con <strong>el número de recogida y su pasaporte</strong>, usted puede ir y recoger su billete de tren en cualquier oficina de la estación. Escribir el número de recogida en un documento o levar una copia impresa será útil.  </p>
  <p align="center"><img border="0" width="642" height="167" src="howTo_clip_image002.jpg" /></p>
  <h3 align="left">3. Encontrar las taquillas de recogida en una estación de ferrocarril </h3>
  <p align="left">Habrá un montón de taquillas en la estación de ferrocarril. Esto le sonará sorprendente pero muy pocos de ellas están abiertas para la recogida de entradas. Así que en primer lugar usted necesita encontrar la ventana correcta para la recogida de entradas. Este tipo de ventanas a menudo están señaladas como "电话订票、网络订票取票窗口 (taquillas de recogida para reserva teléfonicas y online)". Muestre su número de pasaporte (s) y de recogida a la Secretaria y ella o él le entregará el billete de papel. Mantenga siempre su pasaporte (s) y boleto seguro. </p>
  <p align="left">Nota: China Highlights tiene control manual durante la emisión de los billetes como forma de evitar errores. Sin embargo, tan escaso como puede ser quizás (1 de 10.000), todavía puede haber una posibilidad de que usted no puede recoger tickets de tren en el mostrador de billetes (las razones pueden ser que el sistema se actualiza China Rail, o el empleado de billete es novato etc.). En tal caso, la mejor jugada que proponemos es mostrar la imagen de billete emitido al empleado (la captura de pantalla de billete emitido puede encontrarse en el email de confirmación); o contacte con nosotros inmediatamente por teléfono y hablamos al empleado para buscar la solución. Para evitar pérdidas innecesarias, esta situación se fijará inmediatamente antes de la salida. Por favor, comprenda que no hay nada que podamos hacer si nos contactas después de haberse ido el tren. China Highlights no asume ninguna responsabilidad por errores en recogida ticket debido a manejo inadecuado más allá de nuestra cuenta.</p>
  <p align="left"> Para encontrar la ventana correcta, mostrar esta frase a cualquier miembro del personal de la estación:</p>
