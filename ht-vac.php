  <h3>4. Verificar su billete </h3>
  <p align="left"><strong><strong>La comprobación de billetes se detiene 5 minutos antes de la hora de salida.</strong> Asegúrese de que usted consigue todo en la lista antes de la comprobación. Las barreras de tickets en la salida de cada de sala de espera enlaza con la plataforma de embarque. Normalmente hay una pantalla LED encima de la barrera diciendo cual es la situación actual es: espera (正在 候车), control de billete (正在 检票), comprobación sobre la compra del billete (停止检票).</p>
   <p align="left"> Las personas tienden a estar inquietos una vez que llega la "comprobación del billete" pero es importante conocer donde se dirigen: sosteniendo <strong>el billete de papel de color azul</strong>, usted pasará a través de la máquina automática de comprobación; <strong>el de color rojo</strong> es para los inspectores del billete y lo picará con unas tijeras de billete. Guarde su billete una vez que la comprobación se haya hecho.</p>
 <p align="left">  En algunas estaciones de ferrocarril, no siempre se dividen secciones de espera. En este caso, mostrar esta frase a cualquier miembro del personal de la estación, al tiempo que muestra su billete, para encontrar la línea de espera: </p>
