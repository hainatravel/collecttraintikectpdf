  <h2><a name="board_train" id="board_train"></a><a name="Part-2_How_to" id="Part-2_How_to"></a>Parte-2 Cómo subir al tren </h2>
  <h3>1. Asegúrese de tener todo listo </h3>
  <p align="left">  <strong>Su pasaporte (s) y billete (s) de papel</strong> son las dos cosas esenciales para el embarque, junto con su equipaje y otras cosas. Usted tendrá que presentar billete de papel dos veces antes de subir: en primer lugar en el control de seguridad y en la comprobación de billete. Sólo una vez por su pasaporte (s) en el control de seguridad. Poner su pasaporte (s) en una riñonera o bolso mini puede ser útil; sin embargo, usted deberá siempre mantenerlo a salvo después de comprobación de este. <br />
  Para comer en el tren, no importa ya sea un bento o comidas hechas a la medida en el coche comedor, es casi lo mismo que comer en un restaurante. Para viajes de larga distancia, le sugerimos que lleve algo de comida, galletas, fideos instantáneos, frutas o algunas bebidas. Lo que es más, no hay papel higiénico,  ni tapa del inodoro desechable o chanclas en el tren, es mejor empaquetarlos si es necesario.   </p>
  <h3>2. Pasar el control de seguridad </h3>
  <p align="left">Es necesaria la comprobación de seguridad antes de entrar en la sala de espera, que en algunos lugares ocurre incluso antes de entrar en el edificio de la estación. El personal de la estación explora con un dispositivo de rayos x y su equipaje pasará por una máquina de escaneo. Comparativamente, la comprobación de seguridad es menos molesta y resistente que en el aeropuerto. Por ejemplo, elementos líquidos comunes no se prohibirán y los encendedores pueden ir en el tren. </p>
  <p align="left">  Cosas que no pueden llevar: las armas de fuego (pistolas, cuchillos, munición de cualquier calibre en cualquier cantidad, etc.), explosivos (incluyendo fuegos artificiales y pirotecnia) e imitación explosivos o dispositivos, los artículos que contienen gases incapacitantes, líquidos u otras sustancias (incluyendo CS gas aerosoles), sustancias inflamables excepto en cantidades mínimas (por ejemplo, encendedores), gas butano, los artículos que están prohibidos o limitados por el gobierno , animales e insectos y cualquier cosa que puede hacer daño a otros pasajeros y la higiene pública. El equipaje que excede la limitación de la regulación no será llevado en el tren.</p>
  <h3>3. Encontrar el lugar adecuado para esperar </h3>
  <p align="left">La sala de espera es el lugar para esperar el embarque. La mayoría de las estaciones han dividido las secciones de espera para los diferentes trenes, algunos tienen todos los pasajeros que esperan en una gran sala con sólo la comprobación de billete especialmente firmado de manera diferente. Para la mayoría de los trenes de alta velocidad, la sección de espera designada se indica en los billetes de papel (vea la imagen de ejemplo). </p>
