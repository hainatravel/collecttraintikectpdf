  <h2>Parte-3 Consejos de viaje </h2>
  <h3>1. Seguridad</h3>
  <p align="left">Viajar en tren en China es bastante seguro. Sin embargo, como otros estilos de viaje ya sea por el aire o a pié, esta ventaja puede ser mitigada si el conocimiento de la seguridad no es una prioridad. Estos son algunos consejos para la  aumentar la seguridad de viajes tren. <br />
    (1) Se recomienda que usted mantenga sus pertenencias dentro de su vista y nunca deje artículos de valor fuera a la vista. Su dinero en efectivo, tarjetas de crédito, documentos de viaje y otros artículos importantes estarán separados y los mantendrá seguros. Normalmente, una pequeña cantidad de dinero en efectivo, sobre 100RMB, es mucho más de lo suficiente para las necesidades en el tren (por ejemplo, alimentos y otras cosas). Tenga algunos billetes pequeños a mano para que no tenga que abrir y buscar en sus bolsas o maletas.<br />
    (2) Viajar con niños pequeños, es muy trabajoso para los adultos para refrenar y conseguir frenar demasiado entusiasmado con todo, para que puedan correr sobre la plataforma y el coche, o jugar con las puertas del cuarto de baño o el grifo de agua. Normalmente las estaciones no tienen pase especial o plataformas de embarque para los viajeros mayores, pero es poco probable que un senior sea empujado. Aparte de los trenes de alta velocidad que están circulando constantemente, los viajeros mayores deben evitar caminar en el tren ya que la circulación discordante y a veces áspera y frenazos bruscos pueden suceder. <br />
    (3) El agua corriente el agua no es potable. Siempre beba agua embotellada u obtener agua en la despensa de bebidas. Permita que sus hijos sepan esto antes de que pidan una bebida. <br />
  (4) Mantener su atención  sobre sus cosas podría ser fácil durante el día o en rutas cortas. Hay posibilidades de que las cosas sean robadas o perdidas en algunos recorridos en tren durante la noche. Utilice el código de bloqueo en su maleta. Si su bolso o mochila tiene una correa, coloque una pinza sobre la barandilla de la cama o el brazo antes de irse a dormir. Esto le ayudará a levantarse en cada movimiento de la bolsa o mochila. Esconda su dinero, tarjetas y documentos de viaje dentro de su chaqueta y cierre la cremallera. Por supuesto, cierre el compartimento, si es posible.  </p>
  <h3>2. Comer a bordo </h3>
  <p align="left">Asiento de clase superior, asiento de clase Business y asiento de primera clase tienen alimentos gratuitos, mini muffins, bollos y algo de bocados, que queda muy detrás de la norma de una comida adecuada. Puedes pedir en el coche comedor (generalmente en coche 5) o comprar uno cuando pasa el carro de bento (generalmente tibia e insípida). Los precios diferentes en distintos tipos de trenes: una comida hecha por encargo en trenes regulares puede costar entre 30 y 50RMB por plato, la gama de comidas de bento de 15RMB por caja en trenes regulares a 45RMB por caja en trenes de alta velocidad. La mayoría de los trenes de alta velocidad no tienen servicios a medida de los coches comedor. </p>
   <p align="left"> Hágase una caja de picnic antes de salir. Frutas, fideos instantáneos, galletas, refrescos y meriendas pueden ser buenas opciones. Muchas estaciones de tren tienen McDonald’s y KFC cerca; coja una caja de comida antes de subir. Tenga en cuenta que alimentos líquidos, bebidas o salsa puede derramarse durante la comprobación de seguridad. </p>
  <h3>3. El retrete</h3>
  <p align="left">Todos los trenes regulares que no son de alta velocidad estan equipados con inodoros en cuclillas estilo japonés, mientras que en todos los trenes de alta velocidad son de estilo occidental. Los baños se encuentran en ambos extremos de cada coche. Tenga en cuenta que estarán temporalmente cerrados 10 minutos antes de que el tren llegue a una estación y durante el tiempo permanece en esa estación. Así que tan sólo tienes que ir cuando la naturaleza llama y no sabes cuándo va a llegar a una estación. Y probablemente habrá una larga cola de espera, también. </p>
    <p align="left">En los trenes de alta velocidad, pañuelos usados y otros desechos se pondrán en el bote de basura. Sin embargo, en los trenes regulares de no alta velocidad, los tejidos y otros desechos pueden aclararse hacia abajo pisando un pedal. Siempre tenga cosas valiosas con usted, puede ponerlos en el gancho de la parte posterior de la puerta y usar el inodoro. </p>
    Los baños en los trenes de alta velocidad disponen de cubierta de asiento de papel desechable y pañuelos. En el baño hay jabón (o detergente) y papel. Pida a un mayordomo la recarga si se ha terminado. Ninguna de estas cosas se ofrece en los trenes regulares que no son de alta velocidad. Deben estar preparados mentalmente ya que los aseos ocupados pueden ser realmente desagradables en los trenes de largo recorrido. Así, un paquete toallitas de bebé o esterilizador de las manos y los pañuelos deben estar en su lista de equipaje.  <br />
    A los niños jóvenes y viajeros mayores se les aconsejan tener un tutor alrededor al usar el inodoro. Los viajeros mayores utilizarán el cuarto de baño con la mano en barandilla. Y tenga cuidado con cerrarse de golpe la puerta y aplastarse los dedos. El suelo a veces puede ser resbaladizo, mire sus pasos y camine lentamente.</p>
